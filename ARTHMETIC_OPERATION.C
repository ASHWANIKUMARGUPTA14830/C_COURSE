#include <stdio.h>

int main() {
    int a = 10, b = 5;
    int sum, diff, product, quotient;

    sum = a + b;
    diff = a - b;
    product = a * b;
    quotient = a / b;

    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", diff);
    printf("Product: %d\n", product);
    printf("Quotient: %d\n", quotient);

    return 0;
}
